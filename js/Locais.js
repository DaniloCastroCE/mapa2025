    transformarArray(id,array) {
        const locais = (array && array.length > 0) ? array : this.locais
        const box = document.querySelector(`#${id}`)
        box.innerHTML = ''
        locais.forEach(el => {
            let cod = ''

            if (el.ativo) {
                cod += `ativo: ${el.ativo}, `
            } else {
                cod += `ativo: true, `
            }

            if (el.tipo) {
                cod += `tipo: "${el.tipo}", `
            } else {
                cod += `tipo: "não definido", `
            }

            if (el.dvr) {
                cod += `dvr: ["${el.dvr}"], `
            } else {
                cod += 'dvr: "não definido", '
            }

            if (el.locktec) {
                cod += `locktec: "${el.locktec}", `
            } else if (el.end.sigla === "CE" && !el.locktec) {
                cod += 'locktec: "FORTALEZA", '
            } else if (el.end.sigla === "SP" && !el.locktec) {
                cod += 'locktec: "SÃO PAULO", '
            } else if (el.end.sigla === "RJ" && !el.locktec) {
                cod += 'locktec: "RIO DE JANEIRO", '
            } else if (el.end.sigla === "PE" && !el.locktec) {
                cod += 'locktec: "RECIFE", '
            }

            box.innerHTML += `{ nome: "${el.nome}", ${cod}  lat: "${el.lat}", lon: "${el.lon}", 
            <br>&nbsp;&nbsp;&nbsp;&nbsp;end: { rua: '${el.end.rua}', num: '${el.end.num}', bairro: "${el.end.bairro}", cidade: "${el.end.cidade}", sigla: "${el.end.sigla}" } },<br><br>`
        })
    }

    getCountCond(array) {
        const locais = (array && array.length > 0) ? array : this.locais
        const count = {
            ce: 0,
            pe: 0,
            rj: 0,
            sp: 0,
            tt: 0,
        }

        locais.forEach(el => {
            if (el.locktec === 'FORTALEZA' && el.ativo) {
                count.ce++
                count.tt++

            } else if (el.locktec === 'RECIFE' && el.ativo) {
                count.pe++
                count.tt++
            }
            else if (el.locktec === 'SÃO PAULO' && el.ativo) {
                count.sp++
                count.tt++

            } else if (el.locktec === 'RIO DE JANEIRO' && el.ativo) {
                count.rj++
                count.tt++
            }
        })

        return count
    }

    nomeSimples (nome) {
        return nome.normalize("NFD").replace(/[\u0300-\u036f]/g, "").toUpperCase()
    }

    getLocal (nome) {
        return this.locais.filter(local => this.nomeSimples(local.nome).includes( this.nomeSimples(nome) )) 
    }
